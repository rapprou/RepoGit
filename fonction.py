# fonction avec l'aadition


def addition(a, b):
    resultat = a + b
    return resultat


# Utilisation de la fonction
nombre1 = 10
nombre2 = 3
somme = addition(nombre1, nombre2)
print(f"La somme de {nombre1} et {nombre2} est égale à {somme}.")

# function Multiplication


def multiplication(a, b):
    resultat = a * b
    return resultat


# Utilisation de la fonction avec la multiplication

nombre1 = 10
nombre2 = 3
produit = multiplication(nombre1, nombre2)
print(f"Le produit de {nombre1} et {nombre2} est égale à {produit}.")


# function Sustraction


def sustraction(a, b):
    resultat = a - b
    return resultat


# Utilisation de la fonction avec la multiplication

nombre1 = 10
nombre2 = 3
difference = sustraction(nombre1, nombre2)
print(f"Le difference entre {nombre1} et {nombre2} est égale à {difference}.")
